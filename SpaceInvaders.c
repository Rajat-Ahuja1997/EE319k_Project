//If char and big ball occupy the same space, subtract a life from the main character
int checkCollision(char x, char y){
	int k =0;	
	for(k=0; k<6; k++){
		if(bigball[k].alive)
			if(bigball[k].x1+14 >= x && bigball[k].x1 <= x+8)
					if(bigball[k].y1 >= 126 && bigball[k].y1<= 140)
					{
						if(numLives > 0){
							ST7735_FillRect(health[numLives].x1, health[numLives].y1-8, 8, 8, 0x0000);
							despawn(k);
							numLives--;
							return 1;
						}
						
						else{
							finals = score;
							for(k = 0; k < 10000; k++)
								Timer1_Init(&sound, 90);
							return 0;
						}
					}
	}
	return 1;
}
