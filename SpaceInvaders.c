//Actually spawns the large acorns that the char needs to dodge
void spawnBigBalls(void){
	int offset = 0;
	int count =0;
	int  rate = (rand()) % 5;
	int i=0;
	for (i=0; i<5; i++){
		rate = rand() % 5;
		if(bigball[i].alive)
			continue;
		if( rate < 3){
			bigball[i].alive = 1; //random to set
			bigball[i].x1 = 10 + offset; //randomize
			bigball[i].y1= 0;
			count ++;
		}
		else bigball[i].alive = 0;
		offset += 20 ;

	}
	if (count == 6)
		bigball[rand()&5].alive =0;
	//check spawn to make sure if 6 if ==6 delete a random one 
}

